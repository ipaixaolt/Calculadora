    private void handlerBotoes(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_handlerBotoes
       JButton botaoEvento = (JButton) evt.getSource();
              
        switch (botaoEvento.getText()) {
            //Caso seja n√∫meros
            case ".":
            case "0":
            case "1":
            case "2":
            case "3":
            case "4":
            case "5":
            case "6":
            case "7":
            case "8":
            case "9":
                operacoes.concatenar(botaoEvento.getText());
                jTextField1.setText(operacoes.getVisor());
                break;
              
            //Caso sejam operadores
            case "+":
            case "-":
            case "^":
            case "*":
            case "/":
            case "%":
                operacoes.setOperador(botaoEvento.getText());
                jTextField1.setText(operacoes.getVisor());
                break;
             
            //Caso seja o bot√£o de apagar o √∫ltimo d√≠gito
            case "CE":
                operacoes.apagaUltimoDigito();
                jTextField1.setText(operacoes.getVisor());
                break;
                
            //Caso seja o bot√£o de apagar tudo
            case "C":
                operacoes.apagaTudo();
                jTextField1.setText(operacoes.getVisor());
                break;
            
            //Caso seja para realizar a opera√ß√£o
            case "=":
                operacoes.realizaOperacoes();
                jTextField1.setText(operacoes.getVisor());
                break;
            default:
                break;
        }        
    }//GEN-LAST:event_handlerBotoes

